// Function to extract random ISBN values from JSON file and create arrays
function generateRandomISBNArrays(bookData, count = 20) {
  // Extract ISBN values from the JSON objects
  const isbnValues = bookData.map((item) => item.ISBN).filter(Boolean);

  // Generate arrays with 1-20 random ISBNs each
  return Array.from({ length: count }, () => {
    const size = Math.floor(Math.random() * 20) + 1;
    return isbnValues.sort(() => Math.random() - 0.5).slice(0, size);
  });
}
